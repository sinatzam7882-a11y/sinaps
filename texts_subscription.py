# ==================== پیام معرفی پلن‌ها (وقتی کاربر روی «خرید اشتراک» می‌زند) ====================
def build_subscription_intro(active_tiers):
    """
    متن معرفی سطوح اشتراک را می‌سازد. فقط سطح‌هایی که در config.py
    active=True دارند نمایش داده می‌شوند (الان فقط برنزی).
    """
    text = "💳 اشتراک سیناپس\n\n"
    text += (
        "برای استفاده از 💬 مشاوره هوشمند، 📁 درخواست پروژه و 🎨 طراحی بنر "
        "اختصاصی، نیاز به یک اشتراک فعال دارید.\n\n"
    )
    for tier in active_tiers.values():
        text += f"{tier['label']}\n"
        text += f"💰 قیمت: {tier['price']}\n"
        text += f"⏳ مدت: {tier['duration_days']} روز\n"
        for perk in tier["perks"]:
            text += f"   {perk}\n"
        text += "\n"
    text += "👇 لطفاً سطح اشتراک موردنظر خود را انتخاب کنید:"
    return text

