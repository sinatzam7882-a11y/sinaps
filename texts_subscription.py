# ==================== نمایش وضعیت اشتراک در پروفایل/منوی اصلی ====================
def build_subscription_status_line(subscription):
    """یک خط کوتاه برای نمایش وضعیت اشتراک در پیام خوش‌آمدگویی یا پروفایل"""
    status = subscription.get("status")
    if status == "active":
        return f"🟢 اشتراک: فعال تا {subscription.get('expires_at', '-')}"
    if status == "pending":
        return "🟡 اشتراک: در انتظار تایید ادمین"
    if status == "expired":
        return "🔴 اشتراک: منقضی شده — برای ادامه تهیه/تمدید کنید"
    if status == "rejected":
        return "🔴 اشتراک: درخواست قبلی رد شده — می‌توانید دوباره اقدام کنید"
    return "⚪ اشتراک: ندارید"

