def build_admin_project_request_notice(user_id, first_name, last_name, answers):
    """متن پیامی که برای ادمین درباره‌ی درخواست پروژه ارسال می‌شود"""
    text = (
        f"📁 درخواست پروژه‌ی جدید!\n\n"
        f"👤 {first_name} {last_name}\n"
        f"🆔 آیدی تلگرام: {user_id}\n\n"
    )
    labels = {
        "project_title": "🏷 عنوان",
        "project_desc": "📝 توضیحات",
        "project_budget": "💰 بودجه",
        "project_deadline": "⏰ ددلاین",
        "project_phone": "📞 شماره تماس",
    }
    for key, label in labels.items():
        text += f"{label}: {answers.get(key, '')}\n"
    return text

